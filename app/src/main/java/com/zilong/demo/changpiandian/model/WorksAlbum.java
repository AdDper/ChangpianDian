public class WorksAlbum {
    private int album_image;
    private String album_name;
    private int album_saleCount;
    private int album_collectionsCount;

    public int getAlbum_image() {
        return album_image;
    }

    public void setAlbum_image(int album_image) {
        this.album_image = album_image;
    }

    public String getAlbum_name() {
        return album_name;
    }

    public void setAlbum_name(String album_name) {
        this.album_name = album_name;
    }

    public int getAlbum_saleCount() {
        return album_saleCount;
    }

    public void setAlbum_saleCount(int album_saleCount) {
        this.album_saleCount = album_saleCount;
    }

    public int getAlbum_collectionsCount() {
        return album_collectionsCount;
    }

    public void setAlbum_collectionsCount(int album_collectionsCount) {
        this.album_collectionsCount = album_collectionsCount;
    }
}
